        <li class="list">運営者は、利用者に次の各号のいずれかに該当する事由が生じた場合には、何らの催告なしに直ちに登録を抹消することができるものとします。</li>
          <p class="ml-5">(1) 登録内容に虚偽があったなど登録申請段階において登録を承認すべきでない事情があったことが判明した場合<</p>
          <p class="ml-5">(2) 重大な過失又は背信行為があった場合</p>
          <p class="ml-5">(3) 支払いの停止があった場合、又は仮差押、差押、競売、破産手続開始、民事再生手続開始、会社更生手続開始、特別清算開始の申立があった場合</p>
          <p class="ml-5">(4) 手形交換所の取引停止処分を受けた場合</p>
          <p class="ml-5">(5) 公租公課の滞納処分を受けた場合</p>
          <p class="ml-5">(6) その他前各号に準ずるような本規約又は個別契約を継続し難い重大な事由が発生した場合</p>
        <li class="list">運営者は、利用者が前項各号にあたらない本規約のいずれかの条項に違反し、相当期間を定めてなした催告後も、利用者の債務不履行が是正されない場合は、登録を抹消することができるものとします。</li>
        <li class="list">運営者は、登録抹消後一定期間取引内容のデータを残す場合がありますが、当該期間経過後はデータを削除しますので、登録抹消前の問い合わせについては対応できず、免責されるものとします。</li>
        <li class="list">登録抹消後、再度登録を希望する際は、再度登録手続を行う必要があります。利用者は再度の登録手続後、従前のデータが引き継がれないことを予め承諾するものとします。</li>
      </ul><br>

      <p class="font-semibold leading-relaxed mt-4 mb-2">第15条　秘密保持</p>
      <ul class="ml-4 list-disc list-inside leading-relaxed text-sm md:text-base" style="list-style-type: none;">
        <li class="list">運営者及び利用者は、本サービスの利用に関して相手方から開示又は提供された機密情報を善良なる管理者の注意をもって取扱い、事前に書面により相手方の同意を得ることなく、本サービスの目的以外に使用し、又は第三者に開示又は提供してはならないものとします。ただし、個人情報及び顧客情報を除く機密情報のうち、次の各号のいずれかに該当するものについてはこの限りではありません。</li>
          <p class="ml-5">(1) 開示又は提供の前後を問わず公知となった情報<</p>
          <p class="ml-5">(2) 開示又は提供された時点において、既に自己が保有している情報</p>
          <p class="ml-5">(3) 開示又は提供によらず、独自に取得した情報</p>
          <p class="ml-5">(4) 機密保持義務を負うことなく正当な権限を有する第三者から合法的に入手した情報</p>
        <li class="list">運営者は、行政機関又は司法機関から機密情報の開示を要求された場合には、本条の規定にかかわらず、以下の措置を取った上で当該行政機関又は司法機関に対して当該機密情報を開示することができるものとします。</li>
          <p class="ml-5">(1) 利用者に対して当該要求があった旨を遅滞なく書面で通知すること<</p>
          <p class="ml-5">(2) 当該機密情報の内、適法に開示が要求されている部分についてのみ開示すること</p>
          <p class="ml-5">(3) 開示する当該機密情報について秘密としての取り扱いが受けられるよう最善をつくすこと</p>
        <li class="list">本条の機密情報保持義務は、本サービス利用終了後も存続するものとします。</li>
      </ul><br>

      <p class="font-semibold leading-relaxed mt-4 mb-2">第16条　利用者に関する情報の収集、解析及び取扱い</p>
      <ul class="ml-4 list-disc list-inside leading-relaxed text-sm md:text-base" style="list-style-type: none;">
        <li class="list">運営者は、個人が特定されない形式で、登録情報、端末情報、その他利用者に関する情報を、運営者が行う情報配信、統計、その他のサービスに活用し又は第三者に提供するため、利用する場合があります。</li>
        <li class="list">運営者は、本サービスの利用状況を把握するため、Cookieなどの情報収集モジュールを利用して、本サービスの利用情報を収集します。これら情報収集モジュールについては、各提供者の利用規約及びプライバシーポリシー等をご参照ください。</li>
        <li class="list">運営者は、Cookieなどの情報収集モジュール利用により生じた損害については、運営者に故意又は重過失がある場合を除き、責任を負わないものとします。</li>
      </ul><br>

      <p class="font-semibold leading-relaxed mt-4 mb-2">第17条　反社会的勢力の排除</p>
      <ul class="ml-4 list-disc list-inside leading-relaxed text-sm md:text-base" style="list-style-type: none;">
        <li class="list">運営者及び利用者は、現在及び将来にわたり、次の各号のいずれにも該当しないことを表明し保証するものとします。</li>
          <p class="ml-5">(1) 反社会的勢力に該当すること</p>
          <p class="ml-5">(2) 反社会的勢力が経営を支配していると認められる関係を有すること</p>
          <p class="ml-5">(3) 反社会的勢力が経営に実質的に関与していると認められる関係を有すること</p>
          <p class="ml-5">(4) 自己若しくは第三者の不正の利益を図る目的又は第三者に損害を加える目的をもってするなど、不当に反社会的勢力を利用していると認められる関係を有すること</p>
          <p class="ml-5">(5) 反社会的勢力に対して資金等を提供し、又は便宜を供与するなどの関与をしていると認められる関係を有すること</p>
          <p class="ml-5">(6) 役員又は経営に実質的に関与している者が、反社会的勢力と社会的に非難されるべき関係を有すること</p>
          <p class="ml-5">(7) 自己又は第三者をして暴力的要求、脅迫的言動、法的責任を超えた不当な要求、風説の流布・偽計・威力等による他人の信用毀損・業務妨害を行うこと</p>
        <li class="list">運営者又は利用者は、相手方が前項の表明・保証に違反して、前項各号の一にでも該当することが判明したときは、何らの催告をせず、本サービスの提供の停止、及び直ちに本サービスに関するすべての契約を解除することができるとともに、被った損害の賠償を請求することができるものとします。</li>
      </ul><br>
      
      <p class="font-semibold leading-relaxed mt-4 mb-2">第18条　権利及び地位の譲渡等</p>
